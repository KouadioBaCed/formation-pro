// Helper function to determine chapter based on question content
export function getChapterForQuestion(questionNumber: number, questionText: string): string {
  const text = questionText.toLowerCase();

  if (text.includes('corporate governance') || text.includes('board of directors') || text.includes('oecd principles') || text.includes('shareholders') || text.includes('treadway commission')) {
    return 'cfe3-corporate-governance';
  }
  if (text.includes('white-collar') || text.includes('fraud triangle') || text.includes('cressey') || text.includes('criminogenic') || text.includes('occupational fraud') || text.includes('organizational crime') || text.includes('report to the nations') || text.includes('differential association') || text.includes('albrecht')) {
    return 'cfe3-white-collar-crime';
  }
  if (text.includes('auditor') || text.includes('isa 240') || text.includes('internal audit') || text.includes('iia') || text.includes('iosco') || text.includes('piob')) {
    return 'cfe3-auditors-responsibilities';
  }
  if (text.includes('fraud risk management') || text.includes('coso') && text.includes('enterprise risk') || text.includes('risk appetite') || text.includes('audit committee')) {
    return 'cfe3-fraud-risk-management';
  }
  if (text.includes('acfe code') || text.includes('professional ethics') || text.includes('cfe') && text.includes('ethics') || text.includes('confidential') || text.includes('conflict of interest')) {
    return 'cfe3-ethics-fraud-examiners';
  }
  if (text.includes('fraud risk assessment') || text.includes('inherent risk') || text.includes('residual risk') || text.includes('preventive control') || text.includes('detective control')) {
    return 'cfe3-fraud-risk-assessment';
  }
  if (text.includes('management') && (text.includes('responsib') || text.includes('compliance')) || text.includes('document retention') || text.includes('internal control') && text.includes('coso')) {
    return 'cfe3-management-responsibilities';
  }
  if (text.includes('fraud prevention') || text.includes('anti-fraud') || text.includes('ethics policy') || text.includes('hotline') || text.includes('training') || text.includes('tone at the top')) {
    return 'cfe3-fraud-prevention-programs';
  }
  if (text.includes('criminal behavior') || text.includes('social control') || text.includes('behaviorist') || text.includes('skinner') || text.includes('reinforcement') || text.includes('punishment')) {
    return 'cfe3-criminal-behavior';
  }

  // Default based on question ranges
  if (questionNumber <= 30) return 'cfe3-corporate-governance';
  if (questionNumber <= 70) return 'cfe3-white-collar-crime';
  if (questionNumber <= 100) return 'cfe3-auditors-responsibilities';
  if (questionNumber <= 130) return 'cfe3-fraud-risk-management';
  if (questionNumber <= 170) return 'cfe3-ethics-fraud-examiners';
  if (questionNumber <= 210) return 'cfe3-fraud-risk-assessment';
  if (questionNumber <= 240) return 'cfe3-management-responsibilities';
  if (questionNumber <= 270) return 'cfe3-fraud-prevention-programs';
  return 'cfe3-criminal-behavior';
}

