// Helper function to determine chapter based on question content
export function getChapterForQuestion(questionNumber: number, questionText: string): string {
  const text = questionText.toLowerCase();

  if (text.includes('individual rights') || text.includes('search warrant') || text.includes('privacy') || text.includes('defamation') || text.includes('false imprisonment') || text.includes('data protection') || text.includes('gdpr') || text.includes('consent') || text.includes('employee') && text.includes('interview')) {
    return 'cfe3-individual-rights';
  }
  if (text.includes('criminal justice') || text.includes('prosecution') || text.includes('defendant') || text.includes('arrest') || text.includes('sentencing') || text.includes('plea') || text.includes('iccpr') || text.includes('vicarious') || text.includes('deferred prosecution')) {
    return 'cfe3-criminal-justice';
  }
  if (text.includes('testify') || text.includes('expert witness') || text.includes('cross-examination') || text.includes('direct examination') || text.includes('inquisitorial') && text.includes('expert') || text.includes('adversarial') && text.includes('witness')) {
    return 'cfe3-testifying';
  }
  if (text.includes('common law') || text.includes('civil law') || text.includes('jurisdiction') || text.includes('venue') || text.includes('adversarial') || text.includes('inquisitorial') || text.includes('substantive law') || text.includes('procedural law')) {
    return 'cfe3-legal-system-overview';
  }
  if (text.includes('evidence') || text.includes('authentication') || text.includes('hearsay') || text.includes('privilege') || text.includes('chain of custody') || text.includes('circumstantial') || text.includes('direct evidence') || text.includes('demonstrative')) {
    return 'cfe3-evidence-principles';
  }
  if (text.includes('civil') && (text.includes('trial') || text.includes('litigation') || text.includes('plaintiff') || text.includes('remedy') || text.includes('damages')) || text.includes('mediation') || text.includes('arbitration') || text.includes('fidelity insurance')) {
    return 'cfe3-civil-justice';
  }
  if (text.includes('bribery') || text.includes('fcpa') || text.includes('uk bribery') || text.includes('oecd') || text.includes('fraud') && text.includes('element') || text.includes('misrepresentation') || text.includes('conspiracy') || text.includes('perjury') || text.includes('fiduciary')) {
    return 'cfe3-fraud-law';
  }
  if (text.includes('tax') || text.includes('evasion') || text.includes('avoidance') || text.includes('irs') || text.includes('deduction') || text.includes('vat') || text.includes('excise')) {
    return 'cfe3-tax-fraud';
  }
  if (text.includes('money laundering') || text.includes('fatf') || text.includes('placement') || text.includes('layering') || text.includes('integration') || text.includes('smurfing') || text.includes('structuring') || text.includes('front business') || text.includes('alternative remittance')) {
    return 'cfe3-money-laundering';
  }
  if (text.includes('securities') || text.includes('insider trading') || text.includes('iosco') || text.includes('churning') || text.includes('investment contract') || text.includes('broker') || text.includes('stock')) {
    return 'cfe3-securities-fraud';
  }
  if (text.includes('bankruptcy') || text.includes('insolvency') || text.includes('creditor') || text.includes('debtor') || text.includes('liquidation') || text.includes('secured interest') || text.includes('bustout')) {
    return 'cfe3-bankruptcy-fraud';
  }

  // Default based on question ranges
  if (questionNumber <= 50) return 'cfe3-individual-rights';
  if (questionNumber <= 100) return 'cfe3-criminal-justice';
  if (questionNumber <= 140) return 'cfe3-testifying';
  if (questionNumber <= 180) return 'cfe3-legal-system-overview';
  if (questionNumber <= 220) return 'cfe3-evidence-principles';
  if (questionNumber <= 260) return 'cfe3-civil-justice';
  if (questionNumber <= 300) return 'cfe3-fraud-law';
  if (questionNumber <= 320) return 'cfe3-tax-fraud';
  if (questionNumber <= 350) return 'cfe3-money-laundering';
  return 'cfe3-securities-fraud';
}

